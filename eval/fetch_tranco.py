"""Fetch Tranco Top-N domains and emit a JSON ready for the extension.

Tranco (https://tranco-list.eu) is the aggregated research-grade ranking of
the most popular domains, combining CrUX, Farsight, Majestic, Radar, Umbrella.
We use it as the Stage 1 allow-list: any visit to a Tranco-Top-N eTLD+1
short-circuits the cascade as "safe".

Usage:
    uv run python eval/fetch_tranco.py                # default --top 5000
    uv run python eval/fetch_tranco.py --top 100000   # full bundle (~1.5 MB)
    uv run python eval/fetch_tranco.py --list-id 4342X
"""

from __future__ import annotations

import argparse
import csv
import io
import json
import sys
from datetime import datetime, timezone
from pathlib import Path

import httpx


TRANCO_BASE = "https://tranco-list.eu"
DEFAULT_UA = "LocalPhish-Eval/0.1 (academic research; phishing detection extension)"
DEFAULT_OUT = Path(__file__).parent.parent / "extension/src/data/tranco-sample.json"


def fetch_latest_list_id(client: httpx.Client) -> str:
    """Get the latest stable Tranco list ID. The /latest endpoint returns
    today's list once available, otherwise yesterday's."""
    r = client.get(f"{TRANCO_BASE}/api/lists/date/latest", timeout=30)
    r.raise_for_status()
    data = r.json()
    if not data.get("available"):
        raise RuntimeError(f"Tranco list not available: {data}")
    return str(data["list_id"])


def fetch_top_n(client: httpx.Client, list_id: str, top_n: int) -> list[str]:
    """Download the top-N rows and return the domains in rank order."""
    # Tranco hosts CSVs at /download/{list_id}/{N}; the response is plain CSV.
    url = f"{TRANCO_BASE}/download/{list_id}/{top_n}"
    print(f"[tranco] downloading {url}", file=sys.stderr)
    r = client.get(url, timeout=120, follow_redirects=True)
    r.raise_for_status()

    text = r.text
    # Each row: "rank,domain"
    reader = csv.reader(io.StringIO(text))
    domains: list[str] = []
    for row in reader:
        if len(row) < 2:
            continue
        domains.append(row[1].strip().lower())
    if len(domains) < top_n // 2:
        raise RuntimeError(
            f"only got {len(domains)} domains from Tranco — expected ~{top_n}; aborting"
        )
    return domains[:top_n]


def main() -> int:
    parser = argparse.ArgumentParser()
    parser.add_argument("--top", type=int, default=5000, help="number of top domains to keep")
    parser.add_argument(
        "--list-id",
        type=str,
        default=None,
        help="pin to a specific Tranco list ID (default: latest)"
    )
    parser.add_argument("--out", type=Path, default=DEFAULT_OUT)
    args = parser.parse_args()

    with httpx.Client(headers={"User-Agent": DEFAULT_UA}) as client:
        list_id = args.list_id or fetch_latest_list_id(client)
        print(f"[tranco] list_id={list_id}", file=sys.stderr)
        domains = fetch_top_n(client, list_id, args.top)

    payload = {
        "_comment": (
            f"Tranco Top-{len(domains)} eTLD+1 allow-list. "
            "Auto-generated by eval/fetch_tranco.py. "
            "Sources: CrUX + Farsight + Majestic + Radar + Umbrella (Dowdall combined)."
        ),
        "_source": "https://tranco-list.eu",
        "_list_id": list_id,
        "_fetched_at": datetime.now(timezone.utc).isoformat(timespec="seconds"),
        "_size": len(domains),
        "domains": domains
    }

    args.out.parent.mkdir(parents=True, exist_ok=True)
    args.out.write_text(json.dumps(payload, ensure_ascii=False), encoding="utf-8")
    size_kb = args.out.stat().st_size / 1024
    print(f"[tranco] wrote {len(domains)} domains -> {args.out} ({size_kb:.1f} KB)", file=sys.stderr)
    return 0


if __name__ == "__main__":
    raise SystemExit(main())
